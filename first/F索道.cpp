#include <iostream>
using namespace std;

int main() {
    int r, g, b;
    // 输入红、绿、蓝三组学生数量
    cin >> r >> g >> b;

    int t = 0;               // 记录当前是第t分钟（缆车到达的分钟数）
    int last_t = 0;          // 记录最后一辆载有学生的缆车到达的分钟数

    // 只要还有学生没坐上缆车，就继续循环
    while (r > 0 || g > 0 || b > 0) {
        if (t % 3 == 0) {    // 第t分钟到达的是红色缆车
            if (r > 0) {     // 红色组还有学生
                // 缆车最多载2人，剩余学生数减去1或2
                r -= (r >= 2) ? 2 : 1;
                last_t = t;  // 更新最后一辆载学生的缆车时间
            }
        } else if (t % 3 == 1) { // 第t分钟到达的是绿色缆车
            if (g > 0) {
                g -= (g >= 2) ? 2 : 1;
                last_t = t;
            }
        } else { // t%3==2，第t分钟到达的是蓝色缆车
            if (b > 0) {
                b -= (b >= 2) ? 2 : 1;
                last_t = t;
            }
        }
        t++; // 下一分钟的缆车到达
    }

    // 最后一名学生到达山顶的时间 = 最后一辆载学生的缆车到达时间 + 30分钟（缆车运行时间）
    cout << last_t + 30 << endl;
    return 0;
}
